# Newline
print("Hello\nWorld")

# Tab
print("Hello\tWorld")

# Backslash
print("This is a backslash: \\")

# Single quote
print('It\'s a sunny day.')

# Double quote
print("He said, \"Hello!\"")

# Carriage return
print("Hello\rWorld")

# Backspace
print("Hello\bWorld")

# Form feed
print("Hello\fWorld")

# Octal value
print("\110\145\154\154\157")  # Outputs: Hello

# Hexadecimal value
print("\x48\x65\x6c\x6c\x6f")  # Outputs: Hello
